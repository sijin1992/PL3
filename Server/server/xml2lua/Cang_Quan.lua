function Cang_Quan_conf.get_data_by_idx(i)
	if Cang_Quan_conf[i] == nil then return nil
	else
		local temp = ""
		temp = temp..Cang_Quan_conf[i].Quan_ID
		temp = temp..";"
		temp = temp..Cang_Quan_conf[i].Activity_ID
		temp = temp..";"
		temp = temp..Cang_Quan_conf[i].Devote
		temp = temp..";"
		for k,v in ipairs(Cang_Quan_conf[i].Reward_List) do temp = temp..v.."," end
			temp = string.sub(temp, 1, -2)
		temp = temp..";"..Cang_Quan_conf[i].real_idx
		return temp
	end
end

